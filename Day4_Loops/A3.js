//  Do While Loop
//  Task5: print number 1 to 5 using do while loop
let num = 1;
do {
    console.log(num)
    num ++
}while(num <= 5)

// Task6: calculate factorial using do while loop
console.log("Factorial")
let n = 5;
let factorial = 1;

do {
    factorial *= n;
    n--;
}while (n >= 1);
console.log(factorial)


