// Array Methods Basics

let vals = [2,4,6,7,8,9,10,12]


// Task 3: push method to add new element 
vals.push(100)
console.log(vals)


// Task 4: pop method to delete last element
vals.pop()
console.log(vals)


// Task 5: shift to remove first element
vals.shift()
console.log(vals)

// Task 6: unshift to add new element at first position
vals.unshift(500)
console.log(vals)